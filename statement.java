//# Statement of the Project
//## Hospital Appointment Management System
//
//---
//
//        ## Problem Statement
//
//Appointment booking in many small hospitals and clinics is still done manually using notebooks or phone calls, which leads to several problems such as:
//
//        - Double-booking of doctors in the same time slot
//- Difficulty in searching patient history
//- Messy and inconsistent data records
//- Increased waiting time and confusion in scheduling
//
//To solve these issues, a simple software system is required that can record patient details, doctor information, and manage appointments in an organized and efficient way.
//
//---
//
//        ## Scope of the Project
//
//This project includes:
//
//        - Registering patients with essential details such as name, age, gender, and phone number
//- Registering doctors with name and specialization
//- Booking appointments by selecting doctor, date, and time slot
//- Preventing duplicate bookings for the same doctor and time
//- Canceling appointments by ID
//- Persistent storage of all records using files
//
//The system supports console interface only and does not include user authentication or database integration.
//
//        ---
//
//        ## Target Users
//
//- **Receptionist / Front Desk Staff**
//Will use the system to create and manage appointments, patient, and doctor data.
//
//- **Doctors (indirect benefit)**
//Will receive a clear and organized appointment schedule.
//
//        - **Patients (indirect benefit)**
//Will get a seamless appointment experience without confusion or delay.
//
//        ---
//
//        ## High-Level Features
//
//1. Patient Management
//2. Doctor Management
//3. Appointment Booking
//4. Appointment Listing
//5. Appointment Cancellation
//6. File-based persistent storage
//
//These features are sufficient to demonstrate Core Java knowledge with OOP and File Handling.
