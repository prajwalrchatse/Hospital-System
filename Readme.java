//# Hospital Appointment Management System
//
//A Java-based console application designed to manage patients, doctors, and appointment bookings efficiently with persistent storage and proper validation.
//
//        ---
//## Project Overview
//This project aims to eliminate manual appointment handling issues in small hospitals or clinics by providing:
//
//        - Patient registration
//- Doctor record management
//- Appointment booking & cancellation
//- Conflict prevention for time slots
//- Data storage in text files for persistence
//
//Built using Core Java with Object-Oriented Programming and File Handling techniques.
//
//        ---
//
//        ## Features
//
//### Patient Management
//- Add new patient
//- View all patients
//
//### Doctor Management
//- Add new doctor
//- View all doctors
//
//### Appointment Management
//- Book appointments (Date + Time Slot)
//- Prevent double-booking for same doctor/time
//- View full appointment list with linked data
//- Cancel appointments (status updated)
//
//---
//
//        ## Technology Stack
//
//| Element | Details |
//        |--------|---------|
//        | Language | Java |
//        | Concepts Used | OOP, File Handling, Exception Handling |
//        | Data Storage | .txt text files |
//        | Tools | JDK (javac/java) |
